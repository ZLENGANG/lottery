const data = require("./data");
const redArr = [];
const blueArr = [];

data.forEach((item) => {
  const red = item.nums.slice(0, 6);
  const blue = item.nums.slice(6);
  redArr.push(red);
  blueArr.push(blue);
});

function statistics(arr) {
  let obj = {};
  arr.forEach((item) => {
    item.forEach((cItem) => {
      if (!obj[cItem]) {
        obj[cItem] = 1;
      } else {
        obj[cItem] = obj[cItem] + 1;
      }
    });
  });

  const sortObj = {};
  var sortedObjKeys = Object.keys(obj).sort(function (a, b) {
    return obj[b] - obj[a];
  });

  for (var i = 0; i < sortedObjKeys.length; i++) {
    sortObj[sortedObjKeys[i] + "_"] = `${obj[sortedObjKeys[i]]}-${(
      (obj[sortedObjKeys[i]] / (data.length * arr[0].length)) *
      100
    ).toFixed(2)}%`;
  }

  return sortObj;
}

console.log(statistics(redArr));
console.log(statistics(blueArr));
